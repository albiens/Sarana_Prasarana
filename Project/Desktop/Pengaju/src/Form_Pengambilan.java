    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO pengambilan_barang VALUES ('"+fNM.getText()+"','"+fUB.getText()+"','"+fNo.getText()+"','"+fHari.getText()+"','"+fKB.getText()+"','"+fNB.getText()+"','"+fJB.getText()+"','"+fKepsek.getSelectedItem()+"','"+fWakasek.getSelectedItem()+"','"+fAlasan.getText()+"')";
            java.sql.Connection con=(Connection)config.configDB();
            java.sql.PreparedStatement pst=con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
       load_table();
       kosong();
                                          
    }//GEN-LAST:event_jButton3ActionPerformed

