    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO tambahuser VALUES ('"+fNM.getText()+"','"+fNIP.getText()+"','"+fAlmt.getText()+"','"+fJK.getText()+"','"+fTTL.getText()+"','"+fNoHP.getText()+"','"+fUmur.getText()+"','"+fEmail.getText()+"')";
            java.sql.Connection con=(Connection)config.configDB();
            java.sql.PreparedStatement pst=con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
       load_table();
       kosong();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void kosong(){
       fNM.setText(null);
        fNIP.setText(null);
        fAlmt.setText(null);
        fJK.setText(null);
        fTTL.setText(null);
        fNoHP.setText(null);
        fUmur.setText(null);
        fEmail.setText(null);
        }
    
    private void load_table(){ 
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama");
        model.addColumn("NIP");
        model.addColumn("Alamat");
        model.addColumn("Jenis Kelamin");
        model.addColumn("TTL");
        model.addColumn("No HP");
        model.addColumn("Umur");
        model.addColumn("Email");
    }
