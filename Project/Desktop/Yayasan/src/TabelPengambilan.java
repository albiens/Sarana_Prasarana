    public ArrayList<Pengambilan> ListPengambilan(String ValToSearch){
        ArrayList<Pengambilan> pengambilanList = new ArrayList<Pengambilan>();
        
        Statement st;
        ResultSet rs;
        
        try{
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery = "SELECT * FROM `pengambilan_barang` WHERE CONCAT(`Nama`, `UntukBagian`, `No`, `HariTgl`, `KodeBarang`, `NamaBarang`, `JumlahBarang`,`Kepsek`,`Wakasek`, `Alasan`)LIKE'%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            Pengambilan pengambilan;
            
            while(rs.next())
            {
                pengambilan = new Pengambilan(
                rs.getString("Nama"),
                rs.getString("UntukBagian"),
                rs.getInt("No"),
                rs.getString("HariTgl"),
                rs.getString("KodeBarang"),
                rs.getString("NamaBarang"),
                rs.getInt("JumlahBarang"),
                rs.getString("Kepsek"),
                rs.getString("Wakasek"),        
                rs.getString("Alasan")
                );
                pengambilanList.add(pengambilan);
            }
        }catch (Exception ex){
            
        }
        return pengambilanList;
    }
        
    public void findPengambilan(){
        ArrayList<Pengambilan> pengambilan = ListPengambilan(fCari.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Nama","Untuk Bagian","No","Hari/Tgl","Kode Barang","Nama Barang","JumlahBarang","Kepsek","Wakasek","Alasan"});
        Object[] row = new Object[10];
        
        for(int i = 0; i < pengambilan.size(); i++){
            row[0] = pengambilan.get(i).getNama();
            row[1] = pengambilan.get(i).getUntukBagian();
            row[2] = pengambilan.get(i).getNo();
            row[3] = pengambilan.get(i).getHariTgl();
            row[4] = pengambilan.get(i).getKodeBarang();
            row[5] = pengambilan.get(i).getNamaBarang();
            row[6] = pengambilan.get(i).getJumlahBarang();
            row[7] = pengambilan.get(i).getKepsek();
            row[8] = pengambilan.get(i).getWakasek();
            row[9] = pengambilan.get(i).getAlasan();
            model.addRow(row);
        }
        fTabelTampil.setModel(model);
    }
