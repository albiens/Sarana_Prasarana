    private void fTabelTampilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fTabelTampilMouseClicked
        // TODO add your handling code here:
        int index =fTabelTampil.getSelectedRow();
        TableModel model = fTabelTampil.getModel();
        String NM =model.getValueAt(index, 0).toString();
        String UB =model.getValueAt(index, 1).toString();
        String NO =model.getValueAt(index, 2).toString();
        String HT =model.getValueAt(index, 3).toString();
        String KB =model.getValueAt(index, 4).toString();
        String NB =model.getValueAt(index, 5).toString();
        String JB =model.getValueAt(index, 6).toString();
        String AL =model.getValueAt(index, 9).toString();
        
        jtRowData.setVisible(true);
        jtRowData.pack();
        jtRowData.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
        jtRowData.fNM.setText(NM);
        jtRowData.fUB.setText(UB);
        jtRowData.fNo.setText(NO);
        jtRowData.fHari.setText(HT);
        jtRowData.fKB.setText(KB);
        jtRowData.fNB.setText(NB);
        jtRowData.fJB.setText(JB);
        jtRowData.fAlasan.setText(AL);
    }//GEN-LAST:event_fTabelTampilMouseClicked

