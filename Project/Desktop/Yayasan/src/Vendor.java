    public ArrayList<VendorClass> ListVendorClass(String ValToSearch){
        ArrayList<VendorClass> vendorclassList = new ArrayList<VendorClass>();
        
        Statement st;
        ResultSet rs;
        
        try{
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery = "SELECT * FROM `tambahvendor` WHERE CONCAT(`NamaPerusahaan`, `Alamat`, `Email`,`NoTelepon`,`AtasNama`)LIKE'%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            VendorClass vendorclass;
            
            while(rs.next())
            {
                vendorclass = new VendorClass(
                rs.getString("NamaPerusahaan"),
                rs.getString("Alamat"),
                rs.getString("Email"),
                rs.getString("NoTelepon"),
                rs.getString("AtasNama")               
                );
                vendorclassList.add(vendorclass);
            }
        }catch (Exception ex){
            
        }
        return vendorclassList;
    }
        
    public void findVendorClass(){
        ArrayList<VendorClass> vendorclass = ListVendorClass(fCari.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Nama Perusahaan","Alamat","Email","No Telepon","Atas Nama"});
        Object[] row = new Object[5];
        
        for(int i = 0; i < vendorclass.size(); i++){
            row[0] = vendorclass.get(i).getNamaPerusahaan();
            row[1] = vendorclass.get(i).getAlamat();
            row[2] = vendorclass.get(i).getEmail();
            row[3] = vendorclass.get(i).getNoTelepon();
            row[4] = vendorclass.get(i).getAtasNama();
            model.addRow(row);
        }
        fTabelTampil.setModel(model);
    }
