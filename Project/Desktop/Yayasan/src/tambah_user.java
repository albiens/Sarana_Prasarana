            String sql = "INSERT INTO tambahuser VALUES ('"+fnama.getText()+"','"+fnip.getText()+"','"+falt.getText()+"','"+fjk.getSelectedItem()+"','"+fttl.getText()+"','"+ftelp.getText()+"','"+fumur.getText()+"','"+femail.getText()+"')";
