    private void fTglDBKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fTglDBKeyTyped
        // hanya bisa di input oleh angka
        char c= evt.getKeyChar();
        if (!(Character.isDigit(c) || (c==KeyEvent.VK_BACK_SPACE) || c==KeyEvent.VK_DELETE)){
            evt.consume();
        }
    }//GEN-LAST:event_fTglDBKeyTyped

